void key_expansion ( const unsigned char *input, unsigned char *expanded )
{
    // Copy the original key as the first 16 bytes
    for (size_t i = 0; i < 16; i++)
        expanded[i] = input[i];

    int bytes_generated = 16;
    int rcon_iteration = 1;
    unsigned char temp[4];

    while (bytes_generated < 176)
    {
        // Last 4 bytes of the previous key
        for (int i = 0; i < 4; i++)
            temp[i] = expanded[bytes_generated - 4 + i];

        if (bytes_generated % 16 == 0)
        {
            // Rotate left
            unsigned char t = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = t;
            // S-box
            temp[0] = sbox[temp[0]];
            temp[1] = sbox[temp[1]];
            temp[2] = sbox[temp[2]];
            temp[3] = sbox[temp[3]];
            // Rcon
            temp[0] ^= rcon[rcon_iteration++];
        }
        for (int i = 0; i < 4; i++)
        {
            expanded[bytes_generated] = expanded[bytes_generated - 16] ^ temp[i];
            bytes_generated++;
        }
    }
}

